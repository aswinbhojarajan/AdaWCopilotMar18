function PieChart() {
  // Updated allocations: Stocks 55%, Cash 20%, Bonds 15%, Crypto 6%, Commodities 4%
  const createArc = (
    startAngle: number,
    endAngle: number,
    innerRadius: number,
    outerRadius: number,
  ) => {
    const startRad = ((startAngle - 90) * Math.PI) / 180;
    const endRad = ((endAngle - 90) * Math.PI) / 180;

    const x1 = 96 + outerRadius * Math.cos(startRad);
    const y1 = 96 + outerRadius * Math.sin(startRad);
    const x2 = 96 + outerRadius * Math.cos(endRad);
    const y2 = 96 + outerRadius * Math.sin(endRad);
    const x3 = 96 + innerRadius * Math.cos(endRad);
    const y3 = 96 + innerRadius * Math.sin(endRad);
    const x4 = 96 + innerRadius * Math.cos(startRad);
    const y4 = 96 + innerRadius * Math.sin(startRad);

    const largeArc = endAngle - startAngle > 180 ? 1 : 0;

    return `M ${x1} ${y1} A ${outerRadius} ${outerRadius} 0 ${largeArc} 1 ${x2} ${y2} L ${x3} ${y3} A ${innerRadius} ${innerRadius} 0 ${largeArc} 0 ${x4} ${y4} Z`;
  };

  return (
    <div
      className="content-stretch flex flex-col h-[192px] items-start relative shrink-0 w-[287px]"
      data-name="PieChart"
    >
      <div className="h-[192px] overflow-clip relative shrink-0 w-full">
        <svg viewBox="0 0 192 192" className="w-full h-full">
          {/* Stocks 55% - starts at 0, goes 198 degrees */}
          <path d={createArc(0, 198, 48, 80)} fill="#1e3a8a" stroke="white" strokeWidth="2" />
          {/* Cash 20% - starts at 198, goes 72 degrees */}
          <path d={createArc(198, 270, 48, 80)} fill="#059669" stroke="white" strokeWidth="2" />
          {/* Bonds 15% - starts at 270, goes 54 degrees */}
          <path d={createArc(270, 324, 48, 80)} fill="#441316" stroke="white" strokeWidth="2" />
          {/* Crypto 6% - starts at 324, goes 21.6 degrees */}
          <path d={createArc(324, 345.6, 48, 80)} fill="#374151" stroke="white" strokeWidth="2" />
          {/* Commodities 4% - starts at 345.6, goes 14.4 degrees */}
          <path d={createArc(345.6, 360, 48, 80)} fill="#d97706" stroke="white" strokeWidth="2" />
        </svg>
      </div>
    </div>
  );
}

